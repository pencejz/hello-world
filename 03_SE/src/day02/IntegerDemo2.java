package day02;
/**
 * 数字类型包装类都支持两个常量：MAX_VALUE,MIN_VALUE
 * @author Administrator
 *
 */
public class IntegerDemo2 {
	
	public static void main(String[] args) {
		
		//int的最值
		int max = Integer.MAX_VALUE;
		int min = Integer.MIN_VALUE;
		System.out.println(max);
		System.out.println(min);
		//long的最值
		long max1 = Long.MAX_VALUE;
		long min1 = Long.MIN_VALUE;
		System.out.println(max1);
		System.out.println(min1);
		//short的最值
		short max2 = Short.MAX_VALUE;
		short min2 = Short.MIN_VALUE;
		System.out.println(max2);
		System.out.println(min2);
		//byte的最值
		byte max3 = Byte.MAX_VALUE;
		byte min3 = Byte.MIN_VALUE;
		System.out.println(max3);
		System.out.println(min3);
		//double的最值
		double max4 = Double.MAX_VALUE;
		double min4 = Double.MIN_VALUE;
		System.out.println(max4);
		System.out.println(min4);
		//float的最值
		float max5 = Float.MAX_VALUE;
		float min5 = Float.MIN_VALUE;
		System.out.println(max5);
		System.out.println(min5);
		
		
	}
}
